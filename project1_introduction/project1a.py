resistance = 100    # resistance in ohms
current = 0.1       # current in amps

print("Welcome\nThe Wee Ohm's Law Program!")
print("It doesn't do much. \nIt wants to be your friend though.")

print("R = \t",resistance, "\t ohms")
print("I = \t", current, "\t amps")

voltage = resistance * current

print("V = \t", voltage, "\t volts")


